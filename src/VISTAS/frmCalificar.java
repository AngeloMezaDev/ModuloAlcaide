    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        // Verificar que los campos obligatorios estén seleccionados
        String cursoSeleccionado = cmbCurso.getSelectedItem().toString().trim();
        String grupoSeleccionado = cmbGrupo.getSelectedItem().toString().trim();
        java.util.Date fechaLimite = jDateFechaLimite.getDate();

        if (cursoSeleccionado.isEmpty() || grupoSeleccionado.isEmpty() || fechaLimite == null) {
            JOptionPane.showMessageDialog(this, "Por favor, complete todos los campos obligatorios.", "Error", JOptionPane.ERROR_MESSAGE);
            return; // Salir del método si algún campo está vacío
        }

        try {
            // Llamar al método para consultar asignaciones y cargar datos de deberes
            ctrlAsig.cargarDatosDeberes(
                    lblFechaLimite, lblCurso, lblGrupo, lblTitulo, txtDescripcion, txtRespuesta,
                    cmbCurso, cmbGrupo, jDateFechaLimite
            );
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(frmCalificar.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(frmCalificar.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_btnBuscarActionPerformed

